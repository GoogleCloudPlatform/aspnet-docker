# This script will build the specified build.
#   $1, the path to the cloudbuild.yaml to run.
#   $2, repository for the resulting image.

# Exit on error or undefined variable
set -eu

if [ -z "${1:-}" ]; then
    echo "Must specify the cloudbuild.yaml to run."
    exit 1
fi

# If no repo is given get it from the ambient project.
if [ -z "${2:-}" ]; then
    readonly project_id=$(gcloud config list core/project --format="csv[no-heading](core)" | cut -f 2 -d '=')
    readonly repo=gcr.io/${project_id}
    echo "Warning: Using repo ${repo} from ambient project."
else
    readonly repo=$2
fi

# The TAG environment variable can be used to override the time based TAG
# generated by this script. If the TAG variable is used then it will become the
# unique tag for the image.
if [ -z "${TAG:-}" ]; then
  readonly TAG=$(date +"%Y-%m-%d_%H_%M")
fi

# Start the build.
readonly build_dir=$(dirname $1) 
gcloud container builds submit "${build_dir}" \
    --config="$1" \
    --substitutions _DOCKER_NAMESPACE=${repo},_TAG=${TAG}
